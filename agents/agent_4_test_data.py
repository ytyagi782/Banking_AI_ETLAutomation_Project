# main
# --------------------------------------------------------------------------
def generate():
    """Generate the test-data INSERT scripts and return the list of paths."""
    base.banner("STEP 4  -  AI Agent: Generate Test Data + INSERT Scripts")
    print(base.ai_status())

    dataset = _dataset()

    # optional AI positive rows for the two FK-free tables
    extra_branches = _maybe_ai_extra("dbo.SRC_Branches", dataset[0][2], 6,
                                     "BranchID", "BranchCode", "BR")
    if extra_branches:
        dataset[0][3].extend(extra_branches)
    extra_customers = _maybe_ai_extra("dbo.SRC_Customers", dataset[1][2], 7,
                                      "CustomerID", "CustomerCode", "CU")
    if extra_customers:
        dataset[1][3].extend(extra_customers)

    out_dir = base.ensure_dir(base.TESTDATA_DIR)
    per_table = _write_per_table(out_dir, dataset)
    combined = _write_combined(out_dir, dataset)

    total = sum(len(rows) for _, _, _, rows in dataset)
    neg = sum(1 for _, _, _, rows in dataset for r in rows
              if str(r.get("_note", "")).startswith("NEGATIVE"))
    print(f"\n{total} rows ({neg} negative/edge) written to:\n  {out_dir}")
    for p in per_table + [combined]:
        print(f"  - {os.path.basename(p)}")
    print("\nThese scripts are NOT executed automatically. To load them, run:")
    print(f"  sqlcmd -S <server> -E -C -i \"{combined}\"")
    log.info(f"[agent_4] test data generated ({total} rows, {neg} negative)")
    return per_table + [combined]


