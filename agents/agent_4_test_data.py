# SQL literal helpers (same behaviour as GoldenTestData/generate_golden_data.py)
# --------------------------------------------------------------------------
def sql_value(value):
    if value is None:
        return "NULL"
    if isinstance(value, bool):
        return "1" if value else "0"
    if isinstance(value, (int, decimal.Decimal)):
        return str(value)
    if isinstance(value, float):
        return repr(value)
    if isinstance(value, datetime.datetime):
        return "'" + value.strftime("%Y-%m-%d %H:%M:%S.%f")[:-3] + "'"
    if isinstance(value, datetime.date):
        return "'" + value.strftime("%Y-%m-%d") + "'"
    return "'" + str(value).replace("'", "''") + "'"


def build_insert_block(full_table, columns, rows):
    col_list = ", ".join(f"[{c}]" for c in columns)
    lines = []
    for row in rows:
        note = row.get("_note")
        values = ", ".join(sql_value(row[c]) for c in columns)
        line = f"INSERT INTO {full_table} ({col_list}) VALUES ({values});"
        if note:
            line += f"  -- {note}"
        lines.append(line)
    return "\n".join(lines)


def _header(title):
    db_name = config_loader.resolve_database(SOURCE_LOGICAL_DB)
