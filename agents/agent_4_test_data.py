# --------------------------------------------------------------------------
# optional AI enrichment - extra realistic rows for FK-free tables only
# --------------------------------------------------------------------------
def _maybe_ai_extra(full_table, columns, start_id, id_col, code_col, code_prefix):
    """
    Ask Claude for a few extra POSITIVE rows for Branches/Customers (no FK deps),
    so we don't risk breaking referential integrity. Returns a list of row dicts
    with fresh IDs. Never raises; returns [] without a key or on any error.
    """
    if not base.have_api_key():
        return []
    try:
        from pydantic import BaseModel

        class Row(BaseModel):
            values: dict

        class Rows(BaseModel):
            rows: list[Row]

        system = (
            "You generate realistic, valid banking test data. Return 3 extra "
            f"rows for {full_table}. Use ONLY these columns: {', '.join(columns)}. "
            "All values must be valid (proper email, standardised upper-case where "
            "relevant, trimmed text). Dates as YYYY-MM-DD strings."
        )
        user = (f"Give 3 new valid rows. Use {id_col} starting at {start_id} "
                f"(increment by 1) and unique {code_col} like {code_prefix}xxx.")
        result = base.ask_claude_json(system, user, Rows, max_tokens=1500)
        out = []
        for i, r in enumerate(result.rows):
            row = {c: r.values.get(c) for c in columns}
            row[id_col] = start_id + i  # trust our IDs, not the model's
            row["_note"] = "AI-generated positive row"
            out.append(row)
        log.info(f"[agent_4] AI added {len(out)} rows to {full_table}")
        return out
    except Exception as exc:
        log.warning(f"[agent_4] AI extra rows skipped for {full_table} ({exc})")
        return []


# --------------------------------------------------------------------------
# writers
# --------------------------------------------------------------------------
def _write_per_table(out_dir, dataset):
    db_name = config_loader.resolve_database(SOURCE_LOGICAL_DB)
    paths = []
    for prefix, full_table, columns, rows in dataset:
        body = build_insert_block(full_table, columns, rows)
        text = (
            _header(f"INSERT test data into {full_table}")
            + f"USE {db_name};\nGO\n\n" + body + "\nGO\n"
        )
        path = os.path.join(out_dir, f"{prefix}_{full_table.split('.')[-1]}.sql")
        with open(path, "w", encoding="utf-8") as f:
            f.write(text)
        paths.append(path)
        log.info(f"[agent_4] wrote {os.path.basename(path)} ({len(rows)} rows)")
    return paths


def _write_combined(out_dir, dataset):
    """One runnable script: reset all layers -> insert source test data -> load."""
    db_of = config_loader.resolve_database
    parts = [_header("FULL RESET + LOAD using synthetic TEST DATA")]
    parts.append("SET XACT_ABORT ON;")
    parts.append("SET NOCOUNT ON;")
    parts.append("GO\n")

    parts.append("-- STEP 1 : DELETE all data from all 4 layers (children first)")
    for logical, tables in DELETE_ORDER:
        parts.append(f"USE [{db_of(logical)}];")
        parts.append("GO")
        for t in tables:
            parts.append(f"DELETE FROM {t};")
        parts.append("GO\n")

    parts.append("-- STEP 2 : INSERT synthetic test data into SOURCE (parents first)")
    parts.append(f"USE [{db_of(SOURCE_LOGICAL_DB)}];")
    parts.append("GO")
    for _prefix, full_table, columns, rows in dataset:
        parts.append(f"-- ---- {full_table} ({len(rows)} rows) ----")
        parts.append(build_insert_block(full_table, columns, rows))
        parts.append("")
    parts.append("GO\n")

    parts.append("-- STEP 3 : RUN load procedures (Source -> PreStaging -> Staging -> DWH)")
    for title, logical, procs in LOAD_PROC_ORDER:
        parts.append(f"-- ---- {title} ----")
        parts.append(f"USE [{db_of(logical)}];")
        parts.append("GO")
        for p in procs:
            parts.append(f"EXEC {p};")
        parts.append("GO\n")

    parts.append("PRINT 'Synthetic test data loaded through all 4 layers.';")
    parts.append("GO")

    path = os.path.join(out_dir, "insert_all_source_testdata.sql")
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(parts))
    log.info(f"[agent_4] wrote {os.path.basename(path)}")
    return path


